public abstract class AbstractStreamItem implements StreamItem {
	
	protected int id = -1;
	
	protected byte[] img = null;

	public AbstractStreamItem(int keyIndex, byte[] img) {
		super();
		this.id = keyIndex;
		this.img = img;
	}

	@Override
	public byte[] getIcon() {
		return this.img;
	}
}
